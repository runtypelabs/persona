/** Default artifact id for the scripted stream and file card (keep in sync). */
export const FULLSCREEN_ASSISTANT_DEMO_ARTIFACT_ID = "fullscreen-assistant-spotlight-1";

function buildFullscreenAssistantFileCardRaw(artifactId: string): string {
  return JSON.stringify({
    component: "FullscreenAssistantFileCard",
    props: {
      title: "Runtype assistant spotlight",
      subtitle: "Document · MD",
      artifactId
    }
  });
}

function pushArtifactOnlyFrames(
  frames: Uint8Array[],
  options?: { artifactId?: string; title?: string; chunkSize?: number }
): void {
  const id = options?.artifactId ?? FULLSCREEN_ASSISTANT_DEMO_ARTIFACT_ID;
  /** Omit "· MD" here — document preset appends \` · ${kind}\` in the toolbar. */
  const title = options?.title ?? "Runtype assistant spotlight";
  const chunkSize = options?.chunkSize ?? 44;

  enqueueFrame(frames, {
    type: "artifact_start",
    id,
    artifactType: "markdown",
    title
  });
  for (let i = 0; i < FULLSCREEN_ASSISTANT_SPOTLIGHT_MARKDOWN.length; i += chunkSize) {
    enqueueFrame(frames, {
      type: "artifact_delta",
      id,
      delta: FULLSCREEN_ASSISTANT_SPOTLIGHT_MARKDOWN.slice(i, i + chunkSize)
    });
  }
  enqueueFrame(frames, { type: "artifact_complete", id });
}

/**
 * Artifact stream only (no transcript). Kept for focused tests / reuse.
 */
export function createFullscreenAssistantArtifactStream(options?: {
  artifactId?: string;
  title?: string;
  chunkSize?: number;
  delayMs?: number;
}): ReadableStream<Uint8Array> {
  const frames: Uint8Array[] = [];
  pushArtifactOnlyFrames(frames, options);
  return buildDelayedStream(frames, options?.delayMs ?? 42);
}

/**
 * Single scripted stream: transcript lines (via `transcript_insert`) then the same artifact markdown stream.
 */
export function createFullscreenAssistantScriptedStream(options?: {
  artifactId?: string;
  title?: string;
  chunkSize?: number;
  /** Delay between each SSE frame (ms). */
  delayMs?: number;
}): ReadableStream<Uint8Array> {
  const delayMs = options?.delayMs ?? 42;
  const artifactId = options?.artifactId ?? FULLSCREEN_ASSISTANT_DEMO_ARTIFACT_ID;
  const frames: Uint8Array[] = [];
  const now = () => new Date().toISOString();

  enqueueFrame(frames, {
    type: "transcript_insert",
    message: {
      id: "m-intro",
      role: "assistant",
      content:
        "Here is a concise readout. Open the document on the right for the full spotlight draft.",
      createdAt: now()
    }
  });

  // Artifact card is now auto-generated by the widget from artifact_start events.
  // The demo overrides PersonaArtifactCard in the component registry to customize the card.

  enqueueFrame(frames, {
    type: "transcript_insert",
    message: {
      id: "m-user-followup",
      role: "user",
      content: "Tighten the checklist section and add one competitor callout.",
      createdAt: now()
    }
  });

  pushArtifactOnlyFrames(frames, { ...options, artifactId });
  return buildDelayedStream(frames, delayMs);
}
