/**
 * Simulates Runtype-style SSE for the fullscreen assistant demo.
 * Frames use `event: message` and JSON `data` with a top-level `type` field (Persona client stream format).
 */

const encoder = new TextEncoder();

function enqueueFrame(frames: Uint8Array[], payload: Record<string, unknown>): void {
  frames.push(encoder.encode(`event: message\ndata: ${JSON.stringify(payload)}\n\n`));
}

function buildDelayedStream(frames: Uint8Array[], delayMs: number): ReadableStream<Uint8Array> {
  let index = 0;
  return new ReadableStream<Uint8Array>({
    pull(controller) {
      return new Promise<void>((resolve) => {
        setTimeout(() => {
          if (index >= frames.length) {
            controller.close();
          } else {
            controller.enqueue(frames[index]);
            index += 1;
          }
          resolve();
        }, delayMs);
      });
    }
  });
}

